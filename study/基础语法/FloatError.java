package 基础语法;

public class FloatError {

    public static void main(String[] args) {
        //默认double型
        float i = 23.12f;
        System.out.printf("%f\n", i);
        double d = 23.12;
        System.out.printf("%f\n", d);
        long l = 10000000000L;
        System.out.printf("%d\n", l);

    }
}
